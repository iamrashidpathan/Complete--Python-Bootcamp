# Arithmetic :

# Addition
print(2+1)

# Subtraction
print(2-1)

# Multiplication
print(2*3)

# Division
print(3/2)

# Floor Division
print(7 // 4)

# Modulo
print(7 % 4)

# Power
print(2 ** 3)

# Order of operation
print(2 + 10 * 10 +3)

# Can use parentheses to specify orders
print((2+10) * (10+3))


